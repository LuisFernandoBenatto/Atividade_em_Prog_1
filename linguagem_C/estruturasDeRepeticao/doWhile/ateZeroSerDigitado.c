#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <math.h>

int main(){
    int numero;
    int soma = 0;

    printf("Somador de numeros\n");
    do{
        printf("Digite um valor: ");
        scanf("%d",&numero);
        soma += numero;
    }while(numero != 0);
    printf("A soma de todos numeros digitados eh: %d\n", soma);
    system("pause");
    return 0;
}
